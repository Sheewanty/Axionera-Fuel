"use client";

import { useState } from "react";
import { ChevronRight, Download, Eye, FileText, Presentation, Search, X } from "lucide-react";
import Modal from "@/components/ui/Modal";

/* ─── Types ──────────────────────────────────────────────────────────────── */
export interface ReportRunRow {
  id: string;
  title: string;
  templateKey: string;
  stationId: string | null;
  stationName: string | null;
  periodFrom: string;
  periodTo: string;
  generatedByName: string;
  createdAt: string;
  status: string;
  usedOpenAi: boolean;
  audience: string;
  artifacts: { id: string; format: string; fileSize: number; downloadUrl: string }[];
}

interface ReportLibraryClientProps {
  reports: ReportRunRow[];
  stations: { id: string; name: string }[];
}

/* ─── Helpers ────────────────────────────────────────────────────────────── */
function formatFileSize(bytes: number): string {
  if (bytes < 1024) return `${bytes} B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
}

function titleCase(value: string): string {
  return value.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
}

function statusClass(status: string): string {
  if (status === "COMPLETED") return "success";
  if (status === "FAILED") return "danger";
  return "warning";
}

/* ─── Report Detail Drawer ───────────────────────────────────────────────── */
function ReportDetailDrawer({
  report,
  open,
  onClose,
}: {
  report: ReportRunRow | null;
  open: boolean;
  onClose: () => void;
}) {
  if (!report) return null;

  return (
    <Modal open={open} title="Report Details" onClose={onClose} size="md">
      <div style={{ display: "flex", flexDirection: "column", gap: "1rem" }}>
        <dl style={{ display: "grid", gridTemplateColumns: "140px 1fr", gap: "0.5rem 1rem", fontSize: "0.85rem" }}>
          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Title</dt>
          <dd style={{ color: "var(--ax-blue)" }}>{report.title}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Template</dt>
          <dd>{titleCase(report.templateKey)}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Station</dt>
          <dd>{report.stationName ?? "All Stations"}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Period</dt>
          <dd>{report.periodFrom} - {report.periodTo}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Generated By</dt>
          <dd>{report.generatedByName}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Generated At</dt>
          <dd>{report.createdAt}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Audience</dt>
          <dd>{report.audience || "-"}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Status</dt>
          <dd>
            <span className={`status-badge ${statusClass(report.status)}`}>
              {titleCase(report.status)}
            </span>
          </dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>AI Commentary</dt>
          <dd>{report.usedOpenAi ? "Included" : "Not used"}</dd>

          <dt style={{ fontWeight: 600, color: "var(--ax-blue)" }}>Formats</dt>
          <dd>
            {report.artifacts.length === 0
              ? "-"
              : report.artifacts.map((a) => `${a.format} (${formatFileSize(a.fileSize)})`).join(", ")}
          </dd>
        </dl>

        {report.artifacts.length > 0 && (
          <div style={{ display: "flex", gap: "0.5rem", flexWrap: "wrap", paddingTop: "0.5rem", borderTop: "1px solid var(--ax-border)" }}>
            {report.artifacts.map((artifact) => (
              <a
                key={artifact.id}
                className="btn btn-outline btn-sm"
                href={artifact.downloadUrl}
              >
                {artifact.format === "PPTX" ? <Presentation size={14} /> : <FileText size={14} />}
                <Download size={13} />
                {artifact.format}
              </a>
            ))}
          </div>
        )}
      </div>
    </Modal>
  );
}

/* ─── Report Library Client ──────────────────────────────────────────────── */
export default function ReportLibraryClient({ reports, stations }: ReportLibraryClientProps) {
  const [search, setSearch] = useState("");
  const [filterType, setFilterType] = useState("");
  const [filterStation, setFilterStation] = useState("");
  const [filterStatus, setFilterStatus] = useState("");
  const [filterFormat, setFilterFormat] = useState("");
  const [selectedReport, setSelectedReport] = useState<ReportRunRow | null>(null);
  const [drawerOpen, setDrawerOpen] = useState(false);

  const reportTypes = [...new Set(reports.map((r) => r.templateKey))];
  const statuses = [...new Set(reports.map((r) => r.status))];
  const formats = [...new Set(reports.flatMap((r) => r.artifacts.map((a) => a.format)))];

  const hasActiveFilter = search || filterType || filterStation || filterStatus || filterFormat;

  const filtered = reports.filter((r) => {
    if (search && !r.title.toLowerCase().includes(search.toLowerCase())) return false;
    if (filterType && r.templateKey !== filterType) return false;
    if (filterStation && r.stationId !== filterStation) return false;
    if (filterStatus && r.status !== filterStatus) return false;
    if (filterFormat && !r.artifacts.some((a) => a.format === filterFormat)) return false;
    return true;
  });

  const clearFilters = () => {
    setSearch("");
    setFilterType("");
    setFilterStation("");
    setFilterStatus("");
    setFilterFormat("");
  };

  const openDetail = (report: ReportRunRow) => {
    setSelectedReport(report);
    setDrawerOpen(true);
  };

  const columns = [
    "Report Name",
    "Type",
    "Station",
    "Period",
    "Format",
    "Generated By",
    "Generated At",
    "Status",
    "Actions",
  ];

  return (
    <>
      <div className="dash-panel">
        <div className="dash-panel-head">
          <div>
            <div className="dash-panel-title">Generated Reports</div>
            <div className="dash-panel-sub">
              {reports.length} report{reports.length !== 1 ? "s" : ""} generated
              {hasActiveFilter ? ` - ${filtered.length} shown` : ""}
            </div>
          </div>
        </div>

        {/* Filter bar */}
        <div
          style={{
            display: "flex",
            flexWrap: "wrap",
            gap: "0.5rem",
            padding: "0.75rem 1rem",
            borderBottom: "1px solid var(--ax-border)",
            alignItems: "center",
          }}
        >
          <div style={{ position: "relative", flex: "1 1 180px", minWidth: "180px" }}>
            <Search size={14} style={{ position: "absolute", left: "10px", top: "50%", transform: "translateY(-50%)", color: "var(--ax-muted)" }} />
            <input
              className="form-input"
              placeholder="Search by report name..."
              value={search}
              onChange={(e) => setSearch(e.target.value)}
              style={{ paddingLeft: "30px", fontSize: "12.5px" }}
            />
          </div>

          <select
            className="form-select"
            value={filterType}
            onChange={(e) => setFilterType(e.target.value)}
            style={{ flex: "0 1 170px", fontSize: "12.5px" }}
          >
            <option value="">All Types</option>
            {reportTypes.map((t) => (
              <option key={t} value={t}>{titleCase(t)}</option>
            ))}
          </select>

          <select
            className="form-select"
            value={filterStation}
            onChange={(e) => setFilterStation(e.target.value)}
            style={{ flex: "0 1 160px", fontSize: "12.5px" }}
          >
            <option value="">All Stations</option>
            {stations.map((s) => (
              <option key={s.id} value={s.id}>{s.name}</option>
            ))}
          </select>

          <select
            className="form-select"
            value={filterStatus}
            onChange={(e) => setFilterStatus(e.target.value)}
            style={{ flex: "0 1 140px", fontSize: "12.5px" }}
          >
            <option value="">All Statuses</option>
            {statuses.map((s) => (
              <option key={s} value={s}>{titleCase(s)}</option>
            ))}
          </select>

          <select
            className="form-select"
            value={filterFormat}
            onChange={(e) => setFilterFormat(e.target.value)}
            style={{ flex: "0 1 120px", fontSize: "12.5px" }}
          >
            <option value="">All Formats</option>
            {formats.map((f) => (
              <option key={f} value={f}>{f}</option>
            ))}
          </select>

          {hasActiveFilter && (
            <button className="btn btn-outline btn-sm" onClick={clearFilters} title="Clear all filters">
              <X size={13} /> Clear
            </button>
          )}
        </div>

        {/* Table */}
        <div className="table-wrapper">
          <table className="data-table">
            <thead>
              <tr>
                {columns.map((col) => (
                  <th key={col}>{col}</th>
                ))}
              </tr>
            </thead>
            <tbody>
              {reports.length === 0 ? (
                <tr>
                  <td colSpan={columns.length} style={{ textAlign: "center", padding: "3rem 1rem", color: "var(--ax-muted)" }}>
                    <div style={{ fontWeight: 700, fontSize: "0.95rem", marginBottom: "0.35rem" }}>No Reports Generated Yet</div>
                    <div style={{ fontSize: "0.8rem" }}>Use the Reports Hub to generate your first PDF or PPTX report.</div>
                  </td>
                </tr>
              ) : filtered.length === 0 ? (
                <tr>
                  <td colSpan={columns.length} style={{ textAlign: "center", padding: "2.5rem 1rem", color: "var(--ax-muted)" }}>
                    <Search size={24} style={{ marginBottom: "0.5rem", opacity: 0.4 }} />
                    <div style={{ fontWeight: 600, fontSize: "0.9rem", marginBottom: "0.25rem" }}>No Matching Reports</div>
                    <div style={{ fontSize: "0.8rem" }}>
                      Try adjusting your filters or{" "}
                      <button
                        onClick={clearFilters}
                        style={{ color: "var(--ax-gold)", background: "none", border: "none", cursor: "pointer", fontWeight: 600, textDecoration: "underline", fontSize: "inherit" }}
                      >
                        clear all filters
                      </button>.
                    </div>
                  </td>
                </tr>
              ) : (
                filtered.map((r) => (
                  <tr key={r.id}>
                    <td style={{ fontWeight: 600 }}>{r.title}</td>
                    <td>{titleCase(r.templateKey)}</td>
                    <td>{r.stationName ?? "All Stations"}</td>
                    <td style={{ whiteSpace: "nowrap" }}>{r.periodFrom} - {r.periodTo}</td>
                    <td>
                      {r.artifacts.length === 0
                        ? "-"
                        : r.artifacts.map((a) => `${a.format} (${formatFileSize(a.fileSize)})`).join(", ")}
                    </td>
                    <td>{r.generatedByName}</td>
                    <td style={{ whiteSpace: "nowrap" }}>{r.createdAt}</td>
                    <td>
                      <span className={`status-badge ${statusClass(r.status)}`}>
                        {titleCase(r.status)}
                      </span>
                    </td>
                    <td>
                      <div style={{ display: "flex", gap: "0.35rem", flexWrap: "wrap" }}>
                        {r.artifacts.map((a) => (
                          <a key={a.id} className="btn btn-outline btn-sm" href={a.downloadUrl} title={`Download ${a.format}`}>
                            {a.format === "PPTX" ? <Presentation size={13} /> : <FileText size={13} />}
                            {a.format}
                          </a>
                        ))}
                        <button className="btn btn-outline btn-sm" onClick={() => openDetail(r)} title="View details">
                          <Eye size={13} /> <ChevronRight size={12} />
                        </button>
                      </div>
                    </td>
                  </tr>
                ))
              )}
            </tbody>
          </table>
        </div>
      </div>

      <ReportDetailDrawer
        report={selectedReport}
        open={drawerOpen}
        onClose={() => setDrawerOpen(false)}
      />
    </>
  );
}
